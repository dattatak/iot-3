<p>Our research approach is to design and develop reliable, efficient, flexible,
economical, real-time and realistic wellness sensor networks for smart home systems. 
</p><p>
The heterogeneous sensor and actuator nodes based on wireless networking technologies are
deployed into the home environment. These nodes generate real-time data related to the
object usage and movement inside the home, to monitor the health and habits of an individual.
</p><p>
Here, wellness stands for how efficiently someone stays fit in the home environment and
performs his or her daily routine in order to live a long and healthy life. We initiate the
research with the development of the smart home approach and implement it in different
home conditions (different houses) to monitor the activity of an inhabitant for wellness
detection. 
</p><p>
Additionally, our research extends the smart home system to smart buildings and
models the design issues related to the smart building environment; these design issues are
linked with system performance and reliability. Our plans include extending the usability and handling the ISM band interference and attenuation
losses without compromising optimum system performance.
</p><p>
<b>The New Art Tech wireless enabled sensors are designed 
for the new smart and connected home – a network of appliances, 
sensors and entertainment devices – all linked to the set top box and from there to 
smart phones, remote controls, tablets and mobile devices.</b>
